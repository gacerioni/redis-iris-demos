    async def _aexecute_cart_remove_item(self, arguments: dict[str, Any], settings: Any) -> dict[str, Any]:
        identity = self.manifest.identity
        customer_id = os.getenv(identity.id_env_var, identity.default_id)
        dish_id = str(arguments.get("dish_id", "")).strip()
        if not dish_id:
            return {"success": False, "error": "dish_id é obrigatório"}
        qty_raw = arguments.get("qty")
        qty: int | None
        try:
            qty = int(qty_raw) if qty_raw is not None else None
        except (TypeError, ValueError):
            qty = None

        client = create_redis_client(settings)
        cart = self._load_cart(client, customer_id)
        if not cart.get("items"):
            return {"success": False, "error": "O carrinho já está vazio."}

        query = _norm_text(dish_id)
        target = None
        for item in cart["items"]:
            item_nome = _norm_text(item.get("nome"))
            if str(item.get("dish_id")) == dish_id or (query and item_nome and (query in item_nome or item_nome in query)):
                target = item
                break
        if target is None:
            return {
                "success": False,
                "error": f"Item '{dish_id}' não está no carrinho.",
                "itens_no_carrinho": [
                    {"dish_id": i.get("dish_id"), "nome": i.get("nome"), "quantidade": i.get("quantidade")}
                    for i in cart["items"]
                ],
            }

        removed_all = False
        if qty is None or qty >= int(target.get("quantidade", 1) or 1):
            cart["items"] = [i for i in cart["items"] if i is not target]
            removed_all = True
        else:
            target["quantidade"] = int(target.get("quantidade", 1) or 1) - max(1, qty)

        if not cart["items"]:
            cart["merchant_id"] = None
            cart["merchant_nome"] = None

        cart = self._recompute_cart(client, cart)
        self._save_cart(client, cart)
        return {
            "success": True,
            "removed": {"dish_id": target.get("dish_id"), "nome": target.get("nome"), "removed_all": removed_all},
            "cart_empty": not cart["items"],
            "cart": self._cart_payload(cart) if cart["items"] else None,
        }

