    def _recompute_cart(self, client, cart: dict[str, Any]) -> dict[str, Any]:
        """Recompute totals: delivery fee is ZERO when the merchant is a clube
        aiqfome partner, otherwise it comes from the merchant document."""
        subtotal = 0.0
        for item in cart.get("items", []):
            try:
                item_subtotal = round(float(item.get("preco_unitario", 0) or 0) * int(item.get("quantidade", 1) or 1), 2)
            except (TypeError, ValueError):
                item_subtotal = 0.0
            item["subtotal"] = item_subtotal
            subtotal = round(subtotal + item_subtotal, 2)

        taxa = 0.0
        clube = False
        eta = None
        if cart.get("merchant_id"):
            _mkey, merchant = self._find_merchant(client, str(cart["merchant_id"]))
            if merchant:
                cart["merchant_nome"] = _first_field(merchant, ("nome", "name", "razao_social"), cart.get("merchant_nome"))
                clube = bool(_first_field(merchant, ("clube_parceiro", "clube", "is_club_partner"), False))
                if not clube:
                    try:
                        taxa = round(float(_first_field(merchant, ("taxa_entrega", "delivery_fee", "frete"), 0) or 0), 2)
                    except (TypeError, ValueError):
                        taxa = 0.0
                eta = _first_field(merchant, ("eta_min", "tempo_entrega_min", "tempo_medio_min", "eta"))
        cart["subtotal"] = subtotal
        cart["taxa_entrega"] = taxa
        cart["taxa_entrega_gratis_clube"] = clube
        cart["total"] = round(subtotal + taxa, 2)
        cart["eta_min"] = eta
        return cart

