    def _find_order(self, client, customer_id: str, order_id: str) -> tuple[str | None, dict[str, Any] | None]:
        """Locate an order by id, tolerating '#AIQ-8807', 'AIQ-8807' and '8807' spellings."""
        target = _norm_order_id(order_id)
        target_digits = _digits_only(target)
        if not target:
            return None, None
        for key, doc in _scan_json_records(client, ["aiqfome_order:*", "aiqfome_orders:*", "aiqfome*order*:*"]):
            owner = str(_first_field(doc, ("customer_id", "user_id"), ""))
            if owner and owner != customer_id:
                continue
            doc_id = _norm_order_id(_first_field(doc, ("order_id", "pedido_id", "id"), ""))
            if not doc_id:
                continue
            if doc_id == target or (target_digits and _digits_only(doc_id) == target_digits):
                return key, doc
        return None, None

    def _derive_key_prefix(self, client, patterns: Sequence[str], fallback: str) -> str:
        """Reuse the key prefix of an existing record so new records land on the
        same Context Surface index prefix; fall back to the canonical template."""
        for pattern in patterns:
            cursor, keys = client.scan(0, match=pattern, count=10)
            for key in keys:
                key = key.decode() if isinstance(key, bytes) else key
                if ":" in key:
                    return key.rsplit(":", 1)[0]
        return fallback

    def _next_order_id(self, client) -> str:
        """Sequential 4-digit order id in the canonical 'AIQ-XXXX' format."""
        highest = 0
        for _key, doc in _scan_json_records(client, ["aiqfome_order:*", "aiqfome_orders:*", "aiqfome*order*:*"]):
            doc_id = _norm_order_id(_first_field(doc, ("order_id", "pedido_id", "id"), ""))
            match = re.fullmatch(r"AIQ-(\d{4})", doc_id)
            if match:
                highest = max(highest, int(match.group(1)))
        if highest:
            return f"AIQ-{min(highest + 1, 9999):04d}"
        return f"AIQ-{random.randint(1000, 9999)}"

    async def _persist_entity(self, settings: Any, class_names: Sequence[str], key: str | None, record: dict[str, Any]) -> bool:
        """Persist a record via the Context Surface (itau path); surgical
        JSON.SET on the same key as fallback so the demo never shows stale state."""
        from context_surfaces import UnifiedClient

        admin_key = getattr(settings, "ctx_admin_key", None)
        surface_id = getattr(settings, "ctx_surface_id", None)
        if admin_key and surface_id:
            try:
                cls = _load_generated_class_any(class_names)
                async with UnifiedClient() as uc:
                    await uc.import_data(
                        admin_key=admin_key,
                        context_surface_id=surface_id,
                        records=[cls(**record)],
                        on_conflict="overwrite",
                        on_error="fail_fast",
                    )
                return True
            except Exception:  # noqa: BLE001
                pass
        if not key:
            return False
        try:
            client = create_redis_client(settings)
            client.execute_command("JSON.SET", key, "$", json.dumps(record, ensure_ascii=False, default=str))
            return True
        except Exception:  # noqa: BLE001
            return False

