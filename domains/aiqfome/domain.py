    def describe_tool_trace_step(
        self,
        *,
        tool_name: str,
        payload: Any,
        runtime_config: dict[str, Any] | None = None,
    ) -> str | None:
        del runtime_config
        detail = ""
        if isinstance(payload, dict):
            for key in ("query", "text", "topic", "dish_id", "order_id", "item_nome", "customer_id", "qty"):
                value = payload.get(key)
                if value:
                    detail = str(value)
                    break

        if tool_name == self.manifest.identity.tool_name:
            return "Identifica o fominha logado antes de consultar dados."
        if tool_name == "get_current_time":
            return "Pega o horário atual pra comparar com timestamps de pedido e entrega."
        if tool_name == "search_dishes_semantic":
            return f"Busca vetorial no cardápio pela vontade do fominha: {detail or 'busca de pratos'}."
        if tool_name == "cart_view":
            return "Consulta o carrinho do fominha no Redis (itens, totais, previsão)."
        if tool_name == "cart_add_item":
            return f"Adiciona um prato ao carrinho e recalcula os totais: {detail or 'item'}."
        if tool_name == "cart_remove_item":
            return f"Remove um prato do carrinho e recalcula os totais: {detail or 'item'}."
        if tool_name == "cart_clear":
            return "Esvazia o carrinho do fominha."
        if tool_name == "cart_checkout":
            return "Fecha o pedido de verdade no Context Surface (cria o Pedido e limpa o carrinho)."
        if tool_name == "simulate_refund_decision":
            return f"Decide o reembolso na hora lendo o histórico online do fominha: {detail or 'reembolso'}."
        if tool_name == "simulate_next_best_offer":
            return "Roda a recomendação lendo o perfil online do fominha no Redis."
        if tool_name.startswith("search_policy_by_text"):
            return f"Busca políticas aiqfome: {detail or 'busca em políticas'}."
        if tool_name.startswith("filter_customer_by_"):
            return "Consulta o cadastro do fominha."
        if tool_name.startswith("filter_merchant_by_"):
            return "Consulta os restaurantes parceiros (taxa, previsão, clube)."
        if tool_name.startswith("filter_dish_by_"):
            return "Consulta o cardápio dos restaurantes."
        if tool_name.startswith("filter_order_by_"):
            return "Consulta os pedidos do fominha (em rota, entregues, histórico)."
        if tool_name.startswith("filter_courier_by_"):
            return "Consulta o entregador do pedido (nome, veículo, posição)."
        if tool_name.startswith("filter_refundrequest_by_") or tool_name.startswith("filter_refund_request_by_"):
            return "Consulta os reembolsos do fominha."
        if tool_name.startswith("filter_voucher_by_"):
            return "Consulta os vouchers e cupons do fominha."
        if tool_name == "search_customer_memory":
            return "Busca memória durável do fominha: alergias, tradições, pratos xodó."
        if tool_name == "remember_customer_detail":
            return "Salva um fato ou preferência durável do fominha pra próximas conversas."
        return None

