    def describe_tool_trace_step(
        self,
        *,
        tool_name: str,
        payload: Any,
        runtime_config: dict[str, Any] | None = None,
    ) -> str | None:
        del runtime_config
        detail = ""
        if isinstance(payload, dict):
            for key in ("query", "text", "topic", "customer_id", "transaction_id", "dispute_id", "amount", "recipient_name", "favorecido_nome"):
                value = payload.get(key)
                if value:
                    detail = str(value)
                    break

        if tool_name == self.manifest.identity.tool_name:
            return "Identifica o cliente Leet Bank logado antes de consultar dados."
        if tool_name == "get_current_time":
            return "Pega o horário atual pra comparar com timestamps de transação e fatura."
        if tool_name == "simulate_pix_transfer":
            return f"Roda a decisão antifraude no perfil online e executa o Pix no Context Surface: {detail or 'envio Pix'}."
        if tool_name == "create_pix_automatico":
            return f"Cadastra a recorrência de Pix Automático no Context Surface: {detail or 'nova recorrência'}."
        if tool_name == "simulate_next_best_offer":
            return "Roda o next-best-action lendo as features online do cliente no Redis."
        if tool_name == "simulate_collateral_credit":
            return f"Contrata o Crédito Flash com o CDB tokenizado como garantia: {detail or 'contratação'}."
        if tool_name.startswith("search_policy_by_text"):
            return f"Busca políticas Leet Bank: {detail or 'busca em políticas'}."
        if tool_name.startswith("filter_account_by_"):
            return "Consulta a conta e o saldo do cliente."
        if tool_name.startswith("filter_card_by_"):
            return "Consulta os cartões do cliente."
        if tool_name.startswith("filter_transaction_by_"):
            return "Consulta as transações relevantes (fatura, cartão ou cliente)."
        if tool_name.startswith("filter_billingcycle_by_"):
            return "Consulta a fatura aberta ou fechada do cartão."
        if tool_name.startswith("filter_dispute_by_"):
            return "Consulta contestações abertas ou históricas."
        if tool_name.startswith("filter_pixcontact_by_"):
            return "Consulta os contatos Pix frequentes do cliente."
        if tool_name.startswith("filter_pixautomatico_by_"):
            return "Consulta as recorrências de Pix Automático do cliente."
        if tool_name.startswith("filter_rewardsaccount_by_"):
            return "Consulta o saldo e o vencimento dos XP do cliente."
        if tool_name.startswith("filter_supportticket_by_"):
            return "Consulta chamados anteriores do cliente."
        if tool_name.startswith("filter_featurestorerecord_by_") or tool_name.startswith("get_featurestorerecord"):
            return "Consulta os agregados online do cliente no feature store (fonte da verdade dos totais)."
        if tool_name == "search_customer_memory":
            return "Busca memória durável do cliente: preferências, recorrentes reconhecidos."
        if tool_name == "remember_customer_detail":
            return "Salva um fato ou preferência durável do cliente pra próximas conversas."
        return None

