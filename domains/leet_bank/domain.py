def _generate_pix_protocol() -> str:
    """Generate a Pix protocol in the PIXAAAAMMDD-XXXXXX format."""
    today = datetime.now(timezone.utc).strftime("%Y%m%d")
    suffix = "".join(random.choices(string.ascii_uppercase + string.digits, k=6))
    return f"PIX{today}-{suffix}"


def _generate_contract_protocol() -> str:
    """Generate a Credito Flash contract protocol in the LEET-AAAAMMDD-XXXXXX format."""
    today = datetime.now(timezone.utc).strftime("%Y%m%d")
    suffix = "".join(random.choices(string.ascii_uppercase + string.digits, k=6))
    return f"LEET-{today}-{suffix}"


