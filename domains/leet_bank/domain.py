def _read_json(client, key: str) -> dict[str, Any] | None:
    raw = client.execute_command("JSON.GET", key)
    if not raw:
        return None
    raw = raw.decode() if isinstance(raw, bytes) else raw
    return json.loads(raw)


def _brl(value: float) -> str:
    return f"R$ {value:,.2f}".replace(",", "X").replace(".", ",").replace("X", ".")


def _norm_text(value: Any) -> str:
    """Accent-insensitive, case-insensitive normalization for name matching."""
    decomposed = unicodedata.normalize("NFD", str(value or ""))
    return "".join(c for c in decomposed if unicodedata.category(c) != "Mn").casefold().strip()


def _norm_key(value: Any) -> str:
    """Normalize a Pix key for comparison: keep only digits, letters and @."""
    return re.sub(r"[^0-9a-z@.\-]", "", _norm_text(value))


def _digits_only(value: Any) -> str:
    return re.sub(r"\D", "", str(value or ""))


def _first_field(doc: dict[str, Any], names: Sequence[str], default: Any = None) -> Any:
    for name in names:
        if name in doc and doc[name] is not None:
            return doc[name]
    return default


def _feat_num(doc: dict[str, Any], names: Sequence[str], default: float = 0.0) -> float:
    try:
        return float(_first_field(doc, names, default) or default)
    except (TypeError, ValueError):
        return default


def _scan_json_records(client, patterns: Sequence[str]) -> list[tuple[str, dict[str, Any]]]:
    """SCAN the given key patterns and return (key, decoded JSON doc) pairs.

    The data model for this domain is generated in parallel, so key templates
    are resolved defensively at runtime instead of hardcoding a single prefix.
    """
    seen: set[str] = set()
    out: list[tuple[str, dict[str, Any]]] = []
    for pattern in patterns:
        cursor = 0
        while True:
            cursor, keys = client.scan(cursor, match=pattern, count=500)
            for key in keys:
                key = key.decode() if isinstance(key, bytes) else key
                if key in seen:
                    continue
                seen.add(key)
                try:
                    doc = _read_json(client, key)
                except Exception:  # noqa: BLE001
                    doc = None
                if isinstance(doc, dict):
                    out.append((key, doc))
            if cursor == 0:
                break
    return out


def _load_generated_class(class_name: str):
    """Dynamically load a class generated by generate_models.py."""
    module_name = "domains.leet_bank.generated_models"
    try:
        module = importlib.import_module(module_name)
    except ModuleNotFoundError:
        resolved = ROOT / "domains" / "leet_bank" / "generated_models.py"
        spec = importlib.util.spec_from_file_location("leet_bank_generated_models", resolved)
        if spec is None or spec.loader is None:
            raise RuntimeError("Modelos gerados não existem. Rode 'make setup DOMAIN=leet_bank'.")
        module = importlib.util.module_from_spec(spec)
        sys.modules[spec.name] = module
        spec.loader.exec_module(module)
    return getattr(module, class_name)


def _load_generated_class_any(class_names: Sequence[str]):
    """Same as _load_generated_class, tolerating naming variants of the parallel data model."""
    last_error: Exception | None = None
    for class_name in class_names:
        try:
            return _load_generated_class(class_name)
        except (AttributeError, RuntimeError) as exc:
            last_error = exc
    raise RuntimeError(f"Nenhuma das classes {list(class_names)} existe nos modelos gerados: {last_error}")


